/*
  Template Literals - JavaScript

  - Allow embedding expressions inside strings using backticks (` `).
  - Support multi-line strings without using \n.
  - Useful for string interpolation and cleaner syntax.

  Example:
  const name = "ABC";
  const age = 17;
  console.log(`My name is ${name} and I am ${age} years old.`);
  // Output: My name is ABC and I am 17 years old.
*/

// Variables for our template literal demonstration
let userName = "ABC";
let age = 30;

// Using template literal (``) for cleaner string interpolation with ${} syntax
console.log(`My name is ${userName} and my age is ${age} years old.`)
