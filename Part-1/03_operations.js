/*
JavaScript Arithmetic Operations

Arithmetic operations allow you to do basic math with numbers in JavaScript. Here are the common ones:

1. Addition (+)
   Adds two numbers together.
   Example: 5 + 3  // Result: 8

2. Subtraction (-)
   Subtracts one number from another.
   Example: 10 - 4  // Result: 6

3. Multiplication (*)
   Multiplies two numbers.
   Example: 6 * 7  // Result: 42

4. Division (/)
   Divides one number by another.
   Example: 20 / 5  // Result: 4

5. Modulus (%) 
   Gives the remainder after division.
   Example: 10 % 3  // Result: 1

6. Exponentiation (**)
   Raises a number to the power of another.
   Example: 2 ** 3  // Result: 8 (2 to the power of 3)

*/

// Arithmetic Operations
  
  // Addition (+)
  let sum = 5 + 3; // 8
  console.log('Addition (5 + 3):', sum);
  
  // Subtraction (-)
  let difference = 10 - 4; // 6
  console.log('Subtraction (10 - 4):', difference);
  
  // Multiplication (*)
  let product = 6 * 7; // 42
  console.log('Multiplication (6 * 7):', product);
  
  // Division (/)
  // Divides one number by another.
  let quotient = 20 / 5; // 4
  console.log('Division (20 / 5):', quotient);
  
  // Modulus (%)
  // Gives the remainder after division.
  let remainder = 10 % 3; // 1
  console.log('Modulus (10 % 3):', remainder);
  
  // Exponentiation (**)
  // Raises a number to the power of another.
  let power = 2 ** 3; // 8
  console.log('Exponentiation (2 ** 3):', power);
