/*
Unary Operators in JavaScript

Unary operators are operators that operate on a single operand (one value).
They are used to perform various operations like incrementing, negating, checking type, etc.

Common Unary Operators:

1. Increment (++): Increases the operand by 1.
   Pre-increment (++x): Increments first, then returns value.
   Post-increment (x++): Returns value first, then increments.
   Example:
     let a = 4;
     console.log(++a); // Output: 5 (increments first, then prints)
     console.log(a++); // Output: 5 (prints first, then increments)
     console.log(a);   // Output: 6

2. Decrement (--): Decreases the operand by 1.
   Pre-decrement (--x): Decrements first, then returns value.
   Post-decrement (x--): Returns value first, then decrements.
   Example:
     let b = 7;
     console.log(--b); // Output: 6 (decrements first, then prints)
     console.log(b--); // Output: 6 (prints first, then decrements)
     console.log(b);   // Output: 5

3. Logical NOT (!): Converts the operand to boolean and negates it.
   Example:
     let isActive = false;
     !isActive; // true
*/

